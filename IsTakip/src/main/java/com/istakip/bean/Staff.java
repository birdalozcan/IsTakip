package com.istakip.bean;

public class Staff {
	private int uye_id;
	private String ad;
	private String soyad;
	private int gelenissayisi;
	private int devamedenissayisi;
	private int bitenissayisi;
	
	public int getUye_id() {
		return uye_id;
	}
	public void setUye_id(int uye_id) {
		this.uye_id = uye_id;
	}
	public String getAd() {
		return ad;
	}
	public void setAd(String ad) {
		this.ad = ad;
	}
	public String getSoyad() {
		return soyad;
	}
	public void setSoyad(String soyad) {
		this.soyad = soyad;
	}
	public int getGelenissayisi() {
		return gelenissayisi;
	}
	public void setGelenissayisi(int gelenissayisi) {
		this.gelenissayisi = gelenissayisi;
	}
	public int getDevamedenissayisi() {
		return devamedenissayisi;
	}
	public void setDevamedenissayisi(int devamedenissayisi) {
		this.devamedenissayisi = devamedenissayisi;
	}
	public int getBitenissayisi() {
		return bitenissayisi;
	}
	public void setBitenissayisi(int bitenissayisi) {
		this.bitenissayisi = bitenissayisi;
	}
	
}
